// Functions to render font in buffer
static inline void draw_char_in_buffer(uint32_t* buf, int buf_w, int buf_h, int x, int y, char c, uint32_t color, int scale) {
    unsigned char uc = (unsigned char)c;
    if (uc < 32 || uc > 127) uc = 127;
    int idx = uc - 32;

    for (int row = 0; row < 8; row++) {
        uint8_t byte = font8x8[idx][row];
        for (int col = 0; col < 8; col++) {
            if (byte & (1 << (7 - col))) {
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        int px = x + col * scale + sx;
                        int py = y + row * scale + sy;
                        if (px >= 0 && px < buf_w && py >= 0 && py < buf_h) {
                            buf[py * buf_w + px] = color;
                        }
                    }
                }
            }
        }
    }
}

static inline void draw_string_in_buffer(uint32_t* buf, int buf_w, int buf_h, int x, int y, const char* str, uint32_t color, int scale) {
    int cur_x = x;
    while (*str) {
        if (*str == '\n') {
            y += 8 * scale;
            cur_x = x;
        } else {
            draw_char_in_buffer(buf, buf_w, buf_h, cur_x, y, *str, color, scale);
            cur_x += 8 * scale;
        }
        str++;
    }
}

